use serde::{Deserialize, Serialize};

#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "lowercase")]
pub enum Lang {
    En,
    Zh,
}

impl Default for Lang {
    fn default() -> Self {
        Lang::En
    }
}

/// Translation keys. Every user-facing string in the GUI resolves through
/// `t(lang, S::...)`.
#[derive(Debug, Clone, Copy)]
pub enum S {
    WindowTitle,
    NewGroup,
    NewGesture,
    Saved,
    SaveFailedPrefix,
    AddedPrefix,
    NoAppDetected,
    GesturesFor,
    NoGroup,
    MasterOn,
    MasterOff,
    Save,
    Unsaved,
    Synced,
    SelectOrAddGroup,
    GroupName,
    Enabled,
    AssociatedApps,
    AppliesAll,
    LinkedCount,
    AppIdPlaceholder,
    Add,
    ClickTargetWindow,
    PickWindow,
    Groups,
    AddGroup,
    Global,
    PressKeys,
    Record,
    Shortcut,
    Description,
    OptionalDescription,
    Unset,
    Unnamed,
    DragDirection,
    Delete,
    Clear,
    AddGesture,
    Language,
    DirUp,
    DirDown,
    DirLeft,
    DirRight,
    DirUpLeft,
    DirUpRight,
    DirDownLeft,
    DirDownRight,
}

pub fn t(lang: Lang, s: S) -> &'static str {
    match lang {
        Lang::En => en(s),
        Lang::Zh => zh(s),
    }
}

